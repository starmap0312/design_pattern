# Library Oriented Architecture (LOA)
# 1) a set of principles and methodologies for designing and developing software in the form of
#    reusable libraries
# 2) dictates the boundaries of a library that exposes functionality through a set of public APIs
# 3) promotes practices similar to Modular Programming and encourages the maintenance of internal libraries
# 4) promotes separation of concerns and programming to interfaces not implementations
#
# principles of LOA
# 1) a software library implementation must be constrained to only one domain
# 2) a software library that needs to use concepts from a different domain than the one it belongs to
#    must interface and reuse the library corresponding to that domain
# 3) all domain specific software libraries must be maintained and supported with separate life-cycles
#
