// Abbreviation Guidlines 
// 1) remove all nonleading vowels
// ex.
//   computer vs. cmptr
//   screen   vs. scrn
//   apple    vs. appl
//   integer  vs. intgr
// 2) use the first letter or first few letters of each word
// 3) keep the first and last letters of each word
// 4) remove useless suffixes
// ex.
//   -ing, -ed, etc.
// 5) create names that you can pronounce
// ex.
//   xPstn    vs. xPos
//   ndsCmptg vs. needsComp

